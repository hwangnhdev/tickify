public class UserFacebookDTO {
    String id, name, email;

    public UserFacebookDTO() {
    }

    public UserFacebookDTO(String id, String name, String email) {
        this.id = id;
        this.name = name;
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public String toString() {
        return "UserFacebookDTO{" + "id=" + id + ", name=" + name + ", email=" + email + '}';
    }
    
    
}
