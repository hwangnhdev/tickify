package models;

/**
 *
 * @author Tang Thanh Vui - CE180901
 */
public class Province {

    private int code;
    private String name;

    public Province() {
    }

    public Province(int code, String name) {
        this.code = code;
        this.name = name;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Province{" + "code=" + code + ", name=" + name + '}';
    }

}
